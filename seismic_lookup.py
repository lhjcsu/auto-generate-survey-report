# 文登区 的别名兼容 (文登市 → 文登区)
_ALIAS = {"文登市": "文登区"}


def get_seismic_params(district: str, town: str) -> Optional[Dict[str, str]]:
    """
    根据区市县和街道/镇名查询地震动参数。

    Parameters
    ----------
    district : str  区市县名称, 如 "环翠区", "文登区"/"文登市", "荣成市", "乳山市"
    town     : str  街道或镇名称, 如 "怡园街道", "张村镇"

    Returns
    -------
    dict 包含:
        pga         : str  峰值加速度 (g), 如 "0.10"
        period      : str  反应谱特征周期 (s), 如 "0.35"
        intensity   : str  抗震设防烈度, 如 "7"
        group       : str  设计地震分组, 如 "第一组"
        district    : str  区市县
        town        : str  街道/镇

    Returns None if not found.
    """
    # 处理别名
    dist_key = _ALIAS.get(district, district)

    dist_data = WEIHAI_SEISMIC_TABLE.get(dist_key)
    if dist_data is None:
        return None

    entry = dist_data.get(town)
    if entry is None:
        return None

    pga_val, period_val = entry
    pga_str = f"{pga_val:.2f}"
    period_str = f"{period_val:.2f}"

    intensity = PGA_TO_INTENSITY.get(pga_str, "")
    group = PERIOD_TO_GROUP.get(period_str, "")

    return {
        "pga": pga_str,
        "period": period_str,
        "intensity": intensity,
        "group": group,
        "district": dist_key,
        "town": town,
    }


def list_districts() -> list:
    """返回所有支持的区市县名称列表"""
    return list(WEIHAI_SEISMIC_TABLE.keys())


def list_towns(district: str) -> list:
    """返回指定区市县下所有街道/镇名称列表"""
    dist_key = _ALIAS.get(district, district)
    dist_data = WEIHAI_SEISMIC_TABLE.get(dist_key, {})
    return list(dist_data.keys())


